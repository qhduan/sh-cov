def transform(x, y, factors):
    cc = abs(y) / factors[9]

    xt = factors[0] + factors[1] * abs(x)
    yt = \
        factors[2] + \
        factors[3] * cc + \
        factors[4] * cc ** 2 + \
        factors[5] * cc ** 3 + \
        factors[6] * cc ** 4 + \
        factors[7] * cc ** 5 + \
        factors[8] * cc ** 6

    xt *= -1 if x < 0 else 1
    yt *= -1 if y < 0 else 1
    return [xt, yt]


def BD09MCtoBD09(coord):
    x, y = coord
    factors = []
    for i in range(len(MCBAND)):
        if y >= MCBAND[i]:
            factors = MC2LL[i]
            break
    return transform(x, y, factors)
